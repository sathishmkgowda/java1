package basic;

public class Array_homogeneoustype_int
{
	public static void main(String[] args) 
	{
		int [] sathish;       //array declaration
		
		sathish=new int[5];    //array size initialization
		
		 // stores values into array
		sathish[0]=10;        
		sathish[1]=20;
		sathish[2]=30;
		sathish[3]=40;
		sathish[4]=50;
		
		
		
		//array utilization
		System.out.println(sathish[0]); 
		System.out.println(sathish[1]);
		System.out.println(sathish[2]);
		System.out.println(sathish[3]);
		System.out.println(sathish[4]);
		
	}

}
